{
	return bHasRifle;
}

void AGroup24GameCharacter::SetIsMovementEnabled(bool bNewIsMovementEnabled)
{
	bIsMovementEnabled = bNewIsMovementEnabled;
}

bool AGroup24GameCharacter::GetIsMovementEnabled()
{
	return bIsMovementEnabled;
}